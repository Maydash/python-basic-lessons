# Сложение (+)
print("Сложение", 5 + 3)
# Вычитание (-)
print("Вычитание ", 10 - 4)
# Умножение (*)
print("Умножение", 7 * 2)
# Деление (/)
print("Деление", 10 / 3)
# Целочисленное деление (//)
print("Целочисленное деление", 10 // 3)
# Остаток от деления (%)
print("Остаток от деления", 10 % 3)
# Возведение в степень (**)
print("Возведение в степень", 2 ** 3)
