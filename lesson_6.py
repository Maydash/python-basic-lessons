# Сравнительные операции в Python

a = 10
b = 20

# Проверка равенства
print(a == b)  # False (10 не равно 20)

# Проверка неравенства
print(a != b)  # True (10 не равно 20)

# Больше чем
print(a > b)   # False (10 не больше 20)

# Меньше чем
print(a < b)   # True (10 меньше 20)

# Больше или равно
print(a >= b)  # False (10 не больше и не равно 20)

# Меньше или равно
print(a <= b)  # True (10 меньше или равно 20)
