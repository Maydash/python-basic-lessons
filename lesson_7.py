# Логические операции

a = True
b = False

# Логическое И
print(a and b)  # False (т.к. одно из значений False)

# Логическое ИЛИ
print(a or b)   # True (т.к. хотя бы одно значение True)

# Логическое НЕ
print(not a)    # False (инверсия True -> False)
print(not b)    # True (инверсия False -> True)
